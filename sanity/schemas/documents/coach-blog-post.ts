import { defineType, defineField } from 'sanity';

/**
 * Coach's personal blog posts
 * Displayed on their public profile
 */
export const coachBlogPost = defineType({
  name: 'coachBlogPost',
  title: 'Coach Blog Post',
  type: 'document',
  fields: [
    defineField({
      name: 'coach',
      title: 'Coach',
      type: 'reference',
      to: [{ type: 'coachProfile' }],
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'title',
        maxLength: 96,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'excerpt',
      title: 'Excerpt',
      type: 'text',
      rows: 3,
      description: 'Brief summary for listings',
    }),
    defineField({
      name: 'mainImage',
      title: 'Main Image',
      type: 'image',
      options: {
        hotspot: true,
      },
      fields: [
        {
          name: 'alt',
          type: 'string',
          title: 'Alternative text',
        },
      ],
    }),
    defineField({
      name: 'body',
      title: 'Body',
      type: 'blockContent',
    }),
    defineField({
      name: 'tags',
      title: 'Tags',
      type: 'array',
      of: [{ type: 'string' }],
      options: {
        layout: 'tags',
      },
    }),
    defineField({
      name: 'publishedAt',
      title: 'Published At',
      type: 'datetime',
    }),
    defineField({
      name: 'seo',
      title: 'SEO',
      type: 'seo',
    }),
  ],
  preview: {
    select: {
      title: 'title',
      coach: 'coach.name',
      media: 'mainImage',
      date: 'publishedAt',
    },
    prepare({ title, coach, media, date }) {
      return {
        title: title || 'Untitled',
        subtitle: `${coach || 'No coach'} â€¢ ${date ? new Date(date).toLocaleDateString() : 'Draft'}`,
        media,
      };
    },
  },
  orderings: [
    {
      title: 'Published Date, New',
      name: 'publishedAtDesc',
      by: [{ field: 'publishedAt', direction: 'desc' }],
    },
  ],
});
